package com.taobao.terminator.client;

public class TerminatorInitException extends RuntimeException{

	private static final long serialVersionUID = 1621075154716590750L;

	public TerminatorInitException() {
		super();
	}

	public TerminatorInitException(String message, Throwable cause) {
		super(message, cause);
	}

	public TerminatorInitException(String message) {
		super(message);
	}

	public TerminatorInitException(Throwable cause) {
		super(cause);
	}
}
