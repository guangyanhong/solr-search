package com.taobao.terminator.client.index.transmit;

public class IndexTransmitException extends Exception{
	
	private static final long serialVersionUID = 4854634629943554782L;
	
	public IndexTransmitException() {
		super();
	}

	public IndexTransmitException(String message, Throwable cause) {
		super(message, cause);
	}

	public IndexTransmitException(String message) {
		super(message);
	}

	public IndexTransmitException(Throwable cause) {
		super(cause);
	}
	
}
